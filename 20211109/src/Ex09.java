
public class Ex09 {
	// 꽃의 정보
	String kind;  // 멤버필드
	String color;
	String name;
	///메서드
	public void setKind(String kind) {
		this.kind = kind;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return this.name;
	}
	public String getColor() {
		return this.color;
	}
	public String getKind() {
		return this.kind;
	}
}
