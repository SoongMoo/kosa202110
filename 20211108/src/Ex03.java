
public class Ex03 {

	public static void main(String[] args) {
		// 배열의 초기화 값
		System.out.println("int 배열");
		int i [] = new int[3]; // 0으로 초기화
		System.out.println(i[0]);
		System.out.println(i[1]);
		System.out.println(i[2]);
		System.out.println("long 배열");
		long l [] = new long[3]; // 0으로 초기화
		System.out.println(l[0]);
		System.out.println(l[1]);
		System.out.println(l[2]);
		System.out.println("float 배열");
		float f [] = new float[3];
		System.out.println(f[0]);// 0.0으로 초기화
		System.out.println(f[1]);
		System.out.println(f[2]);
		double d [] = new double[3];
		System.out.println(d[0]);// 0.0으로 초기화
		System.out.println(d[1]);
		System.out.println(d[2]);
		System.out.println("boolean 배열");
		boolean b [] = new boolean[3];
		System.out.println(b[0]);//false 
		System.out.println(b[1]);
		System.out.println(b[2]);
		System.out.println("String 배열");
		String s [] = new String[3];
		System.out.println(s[0]); //null 
		System.out.println(s[1]);
		System.out.println(s[2]);
	}

}
